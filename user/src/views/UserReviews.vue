          <div class="review-content">
            <div class="review-header">
              <div class="rating-stars">
                <span v-for="n in 5" :key="n" class="star" :class="{ 'active': n <= review.rating }">★</span>
              </div>
              <span class="review-time">{{ formatTime(review.reviewAt) }}</span>
