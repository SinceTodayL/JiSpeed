              下单时间: {{ formatTime(review.orderInfo.createAt) }}
            </div>
          </div>
        </div>
      </div>
    </div>
  </div>
</template>

<script>
import { ref, onMounted } from 'vue'
import { useRouter } from 'vue-router'
import { reviewAPI } from '@/api/user.js'
import { orderAPI } from '@/api/order.js'
import { merchantAPI } from '@/api/merchant.js'

export default {
  name: 'UserReviews',
  setup() {
    const router = useRouter()
    
    const loading = ref(false)
    const reviews = ref([])
    
    // 获取用户所有评价
    const fetchUserReviews = async () => {
      loading.value = true
      try {
        const userId = localStorage.getItem('userId')
        if (!userId) {
          alert('请先登录')
          router.push('/login')
          return
        }
        
        const response = await reviewAPI.getUserReviews(userId)
        
        if (response && response.data && Array.isArray(response.data)) {
          // 保存评价数据
          reviews.value = response.data

          // 为每条评价加载对应的订单信息
          await Promise.all(
            reviews.value.map(async (review) => {
              try {
                const orderResponse = await orderAPI.getOrderDetail(review.orderId)
                if (orderResponse && orderResponse.data) {
                  review.orderInfo = {
                    ...orderResponse.data,
                    itemCount: orderResponse.data.orderItems?.length || 0
                  }
                  
                  // 获取商家信息
                  if (orderResponse.data.merchantId) {
                    try {
                      const merchantResponse = await merchantAPI.getMerchantById(orderResponse.data.merchantId)
                      if (merchantResponse && merchantResponse.data) {
                        review.merchantName = merchantResponse.data.merchantName
                      }
                    } catch (err) {
                      console.error(`获取商家信息失败 (${orderResponse.data.merchantId}):`, err)
                    }
                  }
                }
              } catch (error) {
                console.error(`加载订单信息失败 (${review.orderId}):`, error)
              }
            })
          )
          
          console.log('加载的评价数据:', reviews.value)
        } else {
          reviews.value = []
        }
      } catch (error) {
        console.error('获取评价列表失败:', error)
        reviews.value = []
      } finally {
        loading.value = false
      }
    }
    
    // 跳转到订单详情
    const goToOrderDetail = (orderId) => {
      if (!orderId) {
        console.error('订单ID不存在')
        return
      }
      console.log('跳转到订单详情页:', orderId)
      router.push(`/orders/${orderId}`)
    }
    
    // 格式化时间
    const formatTime = (timeString) => {
      if (!timeString) return '未知时间'
      
      const date = new Date(timeString)
      const now = new Date()
      const diffMs = now - date
      const diffDays = Math.floor(diffMs / (1000 * 60 * 60 * 24))
      
      if (diffDays === 0) {
        return '今天 ' + date.getHours().toString().padStart(2, '0') + ':' + 
               date.getMinutes().toString().padStart(2, '0')
      } else if (diffDays === 1) {
        return '昨天 ' + date.getHours().toString().padStart(2, '0') + ':' + 
               date.getMinutes().toString().padStart(2, '0')
      } else if (diffDays < 30) {
        return `${diffDays}天前`
      } else {
        return `${date.getFullYear()}-${(date.getMonth() + 1).toString().padStart(2, '0')}-${date.getDate().toString().padStart(2, '0')}`
      }
    }
    
    // 获取订单状态文本
    const getOrderStatusText = (status) => {
      const statusTexts = {
        0: '待付款', // Unpaid
        1: '已支付', // Paid
        2: '已确认收货', // Confirmed
        3: '已评价', // Reviewed
        4: '售后中', // Aftersales
        5: '售后结束', // AftersalesCompleted
        6: '已取消', // Cancelled
        7: '已派单', // Assigned
        8: '配送中' // InDelivery
      }
      return statusTexts[status] || '未知状态'
    }
    
    // 返回上一页
    const goBack = () => {
      router.back()
    }
    
    // 返回首页
    const goToHome = () => {
      router.push('/')
    }
    
    onMounted(() => {
      fetchUserReviews()
    })
    
    return {
      loading,
      reviews,
      goToOrderDetail,
      formatTime,
      getOrderStatusText,
      goBack,
      goToHome
    }
  }
}
</script>

